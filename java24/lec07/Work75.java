    // ↑↑↑ここにコードを追加↑↑↑

    // 2つのdouble型の引数を持つコンストラクタを使用して
    // x座標が8.9で、y座標が10.2の
    // Point75型のインスタンスp3を生成
    // ↓↓↓ここにコードを追加↓↓↓
