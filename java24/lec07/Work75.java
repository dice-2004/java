    // ↑↑↑ここにコードを追加↑↑↑

    // p1の情報をPoint75型のprintInfoメソッドを利用して
    // 実行例どおりに出力
    // ↓↓↓ここにコードを追加↓↓↓
    p1.printInfo("p1");
    // ↑↑↑ここにコードを追加↑↑↑

    // p2の情報をPoint75型のprintInfoメソッドを利用して
    // 実行例どおりに出力
    // ↓↓↓ここにコードを追加↓↓↓
    p2.printInfo("p2");
    // ↑↑↑ここにコードを追加↑↑↑

    // p3の情報をPoint75型のprintInfoメソッドを利用して
    // 実行例どおりに出力
